#include<cstring>
using namespace std;
int N,M;
int main()
{
	scanf("%d%d",&N,&M);
	if((N%2 && M%2 )|| (N==1&&M>2) || (M==1&&N>2) ) puts("-1");
	else if(N==1&&M==2){
		puts("RL");
	}
	else if(M==1 && N==2){
		puts("DU");
	}
	else{
		if(N%2==0){
			for(int i = 0;i < M-1;i++) printf("R");
			for(int i = 1;i < N-1;i++){
				printf("D");
				for(int j = 0;j < M-2;j++) printf("%c",i%2 ? 'L':'R');
			}
			printf("D");
			for(int i = 0;i < M-1;i++) printf("L");
			for(int i = 0;i < N-1;i++) printf("U");
		}
		else{
			for(int i = 0;i < N-1;i++) printf("D");
			for(int i = 1;i < M-1;i++){
				 printf("R");
				for(int j = 0;j < N-2;j++){
					printf("%c",i%2 ? 'U':'D');
				}
			}
			printf("R");
			for(int i = 0;i < N-1;i++) printf("U");
			for(int i = 0;i < M-1;i++) printf("L");
		}
		printf("\n");
	}
	return 0;
}
